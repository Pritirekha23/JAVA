package com.java.core;

class Switch 
{
	public static void main(String[]args)
	{
		char ch='u';
		switch(ch)
		{
		
	case 'a':
	case 'A':
		System.out.println("vowel");
		break;
	case 'e':
	case 'E':
		System.out.println("vowel");
		break;
	case 'i':
	case 'I':
		System.out.println("vowel");
		break;
	case 'o':
	case 'O':
		System.out.println("vowel");
		break;
	case 'u':
	case 'U':
		System.out.println("vowel");
		break;
	default:
		System.out.println("consonant");
		break;
	

}
}
}
	